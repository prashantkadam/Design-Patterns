package studentOrientation.library.utils;


/**
 * Abstract class StudentOrientation
 * @author PRASHANT
 *
 */
public abstract class StudentOrientation {
	
	/**
	 * This method will build campus tour activity
	 */
	abstract public void buildCampusTourActivity();
	/**
	 * This method will build book buy activity
	 */
	abstract public void buildBookBuyActivity();
	/**
	 * This method will build course registration activity
	 */
	abstract public void buildCourseRegistrationActivity();
	/**
	 * This method will build dorm selection activity
	 */
	abstract public void buildDormSelectionActivity();

}
